    private void jButton25ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton25ActionPerformed
        Docente docente;

        /*Traer todos los datos*/
        //personales
        String regPer = txtRegPer.getText();
        String cui = txtCui.getText();
        String nombres = txtNombre.getText();
        String apellidos = txtApellido.getText();
        String telefono = txtTelefono.getText();
        String email = txtEmail.getText();
        String genero = cmbGenero.getSelectedItem().toString();
        //profesionales
        String gradoAcad = cmbGenero.getSelectedItem().toString();

        //Datos titulares
        String fechaIngreso = formato.format(dtFechaIngreso.getDate());
        String fechPromocion = formato.format(dtFechaPromocion.getDate());
        String titularidad = spnTitularidad.getValue().toString();
        int horasContratadas = spnHorasContratadas.getValue().hashCode();

        for (int i = 0; i < tblPlaza.getModel().getRowCount(); i++) {
            plazas.put(tblPlaza.getModel().getValueAt(i, 0).toString(), //llave
                tblPlaza.getModel().getValueAt(i, 1));
        }
        //otros cargos
        otrosCargos = new ArrayList<>();
        for (int i = 0; i < lstOtrosCargos.getModel().getSize(); i++) {
            otrosCargos.add(lstOtrosCargos.getModel().getElementAt(i));
        }
        for (int i = 0; i < tblEvaluaciones.getModel().getRowCount(); i++) {
            evaluacionesTrienio.put(tblEvaluaciones.getModel().getValueAt(i, 0).toString(), //llave
                tblEvaluaciones.getModel().getValueAt(i, 1));
        }
        observaciones = new ArrayList<>();
        for (int i = 0; i < lstObservaciones.getModel().getSize(); i++) {
            observaciones.add(lstObservaciones.getModel().getElementAt(i));
        }
        licenciagoce = new ArrayList<>();
        for (int i = 0; i < lstGoce.getModel().getSize(); i++) {
            licenciagoce.add(lstGoce.getModel().getElementAt(i));
        }
        licenciaSingoce = new ArrayList<>();
        for (int i = 0; i < lstSinGoce.getModel().getSize(); i++) {
            licenciaSingoce.add(lstSinGoce.getModel().getElementAt(i));
        }

        boolean x = Conexion.elimnarTitular(registroPersonal);

        docente = new Titular(fechaIngreso, fechPromocion, titularidad, plazas, otrosCargos, observaciones, licenciagoce, licenciaSingoce,
            regPer, cui, telefono, nombres, apellidos, email, genero, gradoAcad, horasContratadas, evaluacionesTrienio);

        boolean y = Conexion.InsertarDocente("Titular", txtRegPer.getText(), docente);

        if (x && y ) {
            JOptionPane.showMessageDialog(null, "DATOS MODIFICADOS CON EXITO");
            lockDPersonales();
            lockTitular();
        }else{
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al modificar al docente", "Algo anda mal", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton25ActionPerformed

