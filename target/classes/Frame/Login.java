        public static void AgregarATabla(JTable tabla, String llave, String valor) {
        HashMap<String, Object> auxiliar = new HashMap<>();

        //Recuperé el modelo
        for (int i = 0; i < tabla.getModel().getRowCount(); i++) {
            auxiliar.put(tabla.getModel().getValueAt(i, 0).toString(), //llave
                    tabla.getModel().getValueAt(i, 1));
        }

        //Agregar el dato
        auxiliar.put(llave, valor);

        //LLevamos a tabla
        DefaultTableModel modeloEvauaciones = new DefaultTableModel();
        modeloEvauaciones.addColumn("Año");
        modeloEvauaciones.addColumn("Nota");
        for (String i : auxiliar.keySet()) {
            modeloEvauaciones.addRow(new Object[]{i, auxiliar.get(i)}); //Los agrega al modelo
        }
        tabla.setModel(modeloEvauaciones);

    }
        
    public void EditarLista(JList lista, int index) {
        ArrayList<String> aux = new ArrayList<>();
        String texto = lista.getSelectedValue().toString(); //levanto el texto seleccionado
        String textoEditado = "";

        //Obtengo el dato
        textoEditado = JOptionPane.showInputDialog(null, "Edite el item seleccionado", texto);
        if (textoEditado != null && !textoEditado.isEmpty()) {

            //Levanto en arreglo auxiliar
            for (int i = 0; i < lista.getModel().getSize(); i++) {
                aux.add((String) lista.getModel().getElementAt(i));
            }
            //modifico el dato en su lugar
            aux.set(index, textoEditado);

            //Vuelvo a meter a la lista
            DefaultListModel modeloCargos = new DefaultListModel();
            for (int i = 0; i < aux.size(); i++) {
                modeloCargos.addElement(aux.get(i));
            }
            lista.setModel(modeloCargos);
        }
        lista.clearSelection();

    }
    
